    vector<int> nextGreaterElement(vector<int>& nums1, vector<int>& nums2) {
        #define n nums1
        #define M nums2
       unordered_map<int,int> m;
        stack<int> st;
        for(auto t:M)
        {
            while(st.size()&&st.top()<t)
            {
                m[st.top()]=t;
                st.pop();
            }
            st.push(t);
        }
        
       for(auto &t:n)
       m[t]>0?t=m[t]:t=-1;
        return n;
    }
